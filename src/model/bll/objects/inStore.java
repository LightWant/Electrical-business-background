package model.bll.objects;

public class inStore {
	private String ylname;
	private Integer jhpc;
	private Integer ckid;
	private Integer num;
	public void setYlname(String ylname) {
		this.ylname = ylname;
	}
	public String getYlname() {
		return ylname;
	}
	public void setJhpc(Integer jhpc) {
		this.jhpc = jhpc;
	}
	public Integer getJhpc() {
		return jhpc;
	}
	public void setCkid(Integer ckid) {
		this.ckid = ckid;
	}
	public Integer getCkid() {
		return ckid;
	}
	public void setNum(Integer num) {
		this.num = num;
	}
	public Integer getNum() {
		return num;
	}
}
